function DrawShape(Shape, Color, Marker, Line, LineWidth)
XYs = reshape(Shape, [1 length(Shape)]);
jawx = [XYs(77) XYs(1:2:26) XYs(67)];
jawy = [XYs(78) XYs(2:2:26) XYs(68) ];
nosex = XYs(103:2:128);
nosey = XYs(104:2:128);
LineStyle = [Color Marker Line];
hold on;
plot(nosex, nosey, LineStyle, jawx, jawy, LineStyle, 'LineWidth', LineWidth);
rbrowx = XYs(59:2:66);
rbrowy = XYs(60:2:66);
lbrowx = XYs(69:2:76);
lbrowy = XYs(70:2:76);
plot(lbrowx, lbrowy, LineStyle, rbrowx, rbrowy, LineStyle, 'LineWidth', LineWidth);
reyex = [XYs(27:2:42) XYs(27)];
reyey = [XYs(28:2:42) XYs(28)];
% reyex1 = [reyex(2:5) reyex(1) reyex(6:8) reyex(2)];
% reyey1 = [reyey(2:5) reyey(1) reyey(6:8) reyey(2)];
leyex = [XYs(43:2:58) XYs(43)];
leyey = [XYs(44:2:58) XYs(44)];
% leyex1 = [leyex(2:5) leyex(1) leyex(6:8) leyex(2)];
% leyey1 = [leyey(2:5) leyey(1) leyey(6:8) leyey(2)];
plot(leyex, leyey, LineStyle, reyex, reyey, LineStyle, 'LineWidth', LineWidth);
mouthx = XYs(79:2:102);
mouthy = XYs(80:2:102);
mouthx = [mouthx XYs(79)];
mouthy = [mouthy XYs(80)];
plot(mouthx, mouthy, LineStyle, 'LineWidth', LineWidth);
hold off;
